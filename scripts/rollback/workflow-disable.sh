workflow_name: $WORKFLOW_NAME
reason: $REASON
timestamp: $TIMESTAMP
---

# Rollback Incident - Workflow Disable

**Date:** $(date +%Y-%m-%d)
**Time:** $(date +%H:%M:%S)
**Rollback Type:** Workflow Disable
**Workflow:** $WORKFLOW_NAME
**Reason:** $REASON

